animals = [camel, lion, deer, goose, bat, rabbit]

def main():
    while True:
        choice = input('Please enter the number of the habitat you would like to view: > ')
        if choice == 'exit':
            print('See you later!')
            break
        if choice.isdigit():
            index = int(choice)
            if 0 <= index < len(animals):
                print(animals[index])
            else:
                print('No such habitat.')
        else:
            print('Invalid input.')

if __name__ == '__main__':
    main()